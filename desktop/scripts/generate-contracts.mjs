#!/usr/bin/env node
// Generate frontend recipe contract types from the engine's public config
// response. The output is committed so ordinary frontend builds do not need to
// infer Python dataclass fields; --check makes drift a CI failure.
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import path from "node:path";

const here = path.dirname(fileURLToPath(import.meta.url));
const desktop = path.resolve(here, "..");
const root = path.resolve(desktop, "..");
const engineSrc = path.join(root, "engine", "src");
const output = path.join(desktop, "src", "generated", "recipe-contract.ts");
const check = process.argv.includes("--check");

function pythonConfig() {
  const python = process.env.AUVIDE_PYTHON || "python";
  const env = { ...process.env, PYTHONPATH: [engineSrc, process.env.PYTHONPATH].filter(Boolean).join(path.delimiter) };
  const result = spawnSync(python, ["-m", "auvide.cli", "--dump-config"], {
    cwd: root,
    env,
    encoding: "utf8",
  });
  if (result.error || result.status !== 0) {
    throw new Error(`could not read engine config: ${result.error?.message || result.stderr || `exit ${result.status}`}`);
  }
  return JSON.parse(result.stdout);
}

function quote(value) {
  return JSON.stringify(value);
}

function union(values, fallback) {
  const unique = [...new Set([...values, fallback].filter((value) => typeof value === "string"))];
  return unique.length ? unique.map(quote).join(" | ") : "string";
}

function fieldType(field) {
  if (field.key === "grade") return "GradeKnobs";
  if (field.type === "boolean") return "boolean";
  if (field.type === "integer" || field.type === "number") return "number";
  if (field.type === "enum" || (field.type === "string" && Array.isArray(field.options) && field.options.length)) {
    return union(field.options || [], field.default);
  }
  return "string";
}

function render(config) {
  const schema = config.recipe_schema;
  if (!schema || schema.schema !== "auvide.recipe-schema" || !Array.isArray(schema.fields)) {
    throw new Error("engine config did not include a valid auvide.recipe-schema response");
  }
  const gradeKnobs = config.grade_knobs.map(quote).join(" | ") || "never";
  const fields = schema.fields.map((field) => `  ${quote(field.key)}: ${fieldType(field)};`).join("\n");
  return `// Generated by scripts/generate-contracts.mjs; do not edit by hand.\n// Source: engine --dump-config recipe_schema (${schema.schema} v${schema.version}).\n\nexport type GradeKnob = ${gradeKnobs};\nexport type GradeKnobs = Record<GradeKnob, number> & Record<string, number>;\n\nexport interface Recipe {\n${fields}\n}\n\nexport interface RecipeField {\n  key: keyof Recipe;\n  label: string;\n  type: \"boolean\" | \"integer\" | \"number\" | \"string\" | \"enum\" | \"object\";\n  section: string;\n  default: Recipe[keyof Recipe];\n  options?: Array<string | number>;\n  minimum?: number;\n  maximum?: number;\n  advanced?: boolean;\n}\n\nexport interface RecipeSchema {\n  schema: \"auvide.recipe-schema\";\n  version: number;\n  recipe_schema: \"auvide.recipe\";\n  recipe_version: number;\n  fields: RecipeField[];\n}\n\nexport interface Config {\n  styles: Record<string, Recipe>;\n  targets: string[];\n  grade_knobs: GradeKnob[];\n  models: string[];\n  recipe_schema: RecipeSchema;\n}\n`;
}

const generated = render(pythonConfig());
const current = existsSync(output) ? readFileSync(output, "utf8") : "";
if (check) {
  if (current !== generated) {
    console.error("[generate-contracts] generated recipe contract is stale; run: bun run generate-contracts");
    process.exit(1);
  }
  console.log("[generate-contracts] recipe contract is current");
} else {
  mkdirSync(path.dirname(output), { recursive: true });
  writeFileSync(output, generated);
  console.log(`[generate-contracts] ${output}`);
}
